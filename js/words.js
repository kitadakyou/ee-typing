// 配列をシャッフルした新配列を返す（Fisher-Yates）。
export function shuffled(arr) {
  const a = arr.slice();
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

// 作品をシャッフルして詰め合わせ、1テストにする。
// 1出題＝1作品。作品を1つ打ち終えた時点で、display の合計字数が 400 を超えたら停止（最低1作品）。
// 返り値の各要素は { source, sentences }（sentences は {display, reading} の配列）。
export function packPassages(pool) {
  const works = shuffled(pool);
  const picked = [];
  let total = 0;
  for (const w of works) {
    picked.push({ source: `${w.author} 『${w.title}』 より`, sentences: w.sentences });
    total += w.sentences.reduce((sum, s) => sum + s.display.length, 0);
    if (total > 400) break; // 作品を打ち終えて 400 超で停止
  }
  return picked;
}
