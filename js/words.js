// 配列をシャッフルした新配列を返す（Fisher-Yates）。
export function shuffled(arr) {
  const a = arr.slice();
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

// 段落を詰め合わせて1テストにする。display の合計字数が 400 を超えたら停止（最低1段落）。
// ただし 600 字を超える追加はしない（次の短い段落を試す）。
export function packPassages(pool) {
  const order = shuffled(pool);
  const picked = [];
  let total = 0;
  for (const p of order) {
    if (picked.length && total + p.display.length > 600) continue; // 600 上限ガード
    picked.push(p);
    total += p.display.length;
    if (total > 400) break; // 400 超で停止
  }
  return picked;
}
